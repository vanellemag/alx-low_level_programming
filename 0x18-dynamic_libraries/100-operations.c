#include <stdio.h>
/**
 * add - add two number
 * @a: int
 * @b: int
 * Description: summarise two integer
 * Return: sum of two number
 **/
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtraction of two numbers
 * @a: int
 * @b: int
 * Return: substraction of the two number
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplied two numbers
 * @a: int
 * @b: int
 * Return: multiplcation of two numbers
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divided two integer
 * @a: int
 * @b: int
 * Return: division of two integer
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulation of two numbers
 * @a: int
 * @b: int
 * Return: do modulation of two numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
